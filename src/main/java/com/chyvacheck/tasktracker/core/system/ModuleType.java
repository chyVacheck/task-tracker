
/**
 * @file ModuleType.java
 * 
 * @description
 * Перечисление всех возможных типов модулей приложения.
 * Используется для классификации компонентов системы:
 * контроллеры, сервисы, репозитории, мапперы, middleware и др.
 * 
 * @details
 * Назначение данного перечисления:
 * - Помогает явно определять тип каждого системного модуля
 * - Используется в базовых классах модулей (BaseModule) для мета-информации
 * - Может применяться для логирования, мониторинга, трассировки
 * 
 * Примеры типов:
 * - CONTROLLER: для REST API контроллеров
 * - SERVICE: для бизнес-логики
 * - REPOSITORY: для доступа к данным
 * - MAPPER: для конвертации между сущностями и DTO
 * - CACHE: для кеширования данных
 * - MIDDLEWARE: для обработки запросов
 * - DATABASE: для системных операций с БД
 * - SYSTEM: для внутренних подсистем
 * - UNKNOWN: тип по умолчанию для неизвестных модулей
 * 
 * @author
 * Dmytro Shakh
 */

package com.chyvacheck.tasktracker.core.system;

/**
 * Перечисление типов модулей в системе.
 */
public enum ModuleType {
	CONTROLLER, // Контроллеры REST API
	SERVICE, // Сервисы бизнес-логики
	REPOSITORY, // Репозитории (доступ к данным, базы данных)
	MAPPER, // Мапперы DTO ↔ Entity
	CACHE, // Модули кеширования данных
	MIDDLEWARE, // Middleware (обработка запроса/ответа)
	DATABASE, // Модули для работы с базами данных
	SYSTEM, // Системные внутренние модули
	UNKNOWN // Неопределённый или нераспознанный модуль
}